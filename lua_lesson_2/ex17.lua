-- Какого типа следующие выражения?



-- --1
print(type(true or false)) -- Тип boolean

--2
print(type(1 + 5 / 2)) -- Тип number

--3
print(type('word' .. ' and ' .. 'slovo')) -- Тип стринг

--4
print(type('word' > 'slovo')) -- Тип boolean

--5
print(type(1 < 5)) -- Тип boolean

--6
print(type(1 == 1)) -- Тип boolean

--7
print(type(a >= b)) -- Тип boolean

